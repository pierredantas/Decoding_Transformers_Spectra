"""
All graph sections are self-contained and re-define the same MP helper
functions locally.  The canonical docstrings are given here; they apply
to every identical copy throughout the file.

ALBERT-specific note on β
--------------------------
* Square matrices (Q, K, V, Att-Dense, 768×768): β_override = 1.00
* Rectangular matrices (FFN 3072×768, FFN-Out 768×3072): β_override = 0.25
* Embedding mapping (768×128): β_override ≈ 0.17 (used in Graph 11 only)

β is always overridden explicitly in SETTINGS to ensure the correct MP law
is applied, rather than computed from the actual matrix dimensions.
"""


def _mp_support(beta: float):
    """
    Return the lower and upper bulk-edge support points of the MP distribution.

        a = (1 − √β)²    b = (1 + √β)²

    Parameters
    ----------
    beta : float
        Aspect ratio β = min(m, n) / max(m, n),  0 < β ≤ 1.

    Returns
    -------
    a, b : float
        Lower and upper MP bulk support edges.
    """
    r = np.sqrt(beta)
    return (1 - r)**2, (1 + r)**2


def _mp_pdf(x, beta: float, a: float, b: float) -> np.ndarray:
    """
    Evaluate the Marchenko-Pastur PDF.

    ρ_MP(λ; β) = √[(b−λ)(λ−a)] / (2π β λ)   for λ ∈ [a, b], else 0.

    Parameters
    ----------
    x : array_like
        Evaluation points (eigenvalues λ).
    beta : float
        MP shape parameter.
    a, b : float
        Lower and upper support edges.

    Returns
    -------
    np.ndarray
        PDF values at ``x``.  Zero outside [a, b].
    """
    x   = np.asarray(x, dtype=np.float64)
    out = np.zeros_like(x)
    m   = (x >= a) & (x <= b)
    xm  = np.clip(x[m], 1e-15, None)   # avoid division by zero at λ=0
    out[m] = np.sqrt((b - xm) * (xm - a)) / (2 * np.pi * beta * xm)
    return out


def _cumtrapz_np(y: np.ndarray, x: np.ndarray) -> np.ndarray:
    """
    Cumulative trapezoidal integration (no SciPy dependency).

    Parameters
    ----------
    y : np.ndarray
        Integrand values, same length as ``x``.
    x : np.ndarray
        Abscissa values.

    Returns
    -------
    np.ndarray
        Cumulative integral starting at 0, same length as ``y``.
    """
    dx  = np.diff(x)
    seg = 0.5 * (y[:-1] + y[1:]) * dx
    return np.concatenate([[0.0], np.cumsum(seg)])


def _mp_cdf(x, beta: float, grid_points: int = 8192) -> np.ndarray:
    """
    Evaluate the Marchenko-Pastur CDF numerically.

    Uses the substitution g(t) = a + (b−a)·t² to concentrate quadrature
    points near the lower edge where the PDF has an integrable singularity.

    Parameters
    ----------
    x : array_like
        Evaluation points.
    beta : float
        MP shape parameter.
    grid_points : int
        Number of quadrature points (default 8192).

    Returns
    -------
    np.ndarray
        CDF values in [0, 1].
    """
    a, b = _mp_support(beta)
    t    = np.linspace(0.0, 1.0, grid_points)
    g    = a + (b - a) * t * t   # substitution resolves the lower-edge singularity
    pdf  = _mp_pdf(g, beta, a, b)
    cdf_vals = _cumtrapz_np(pdf, g)
    cdf_vals /= cdf_vals[-1]     # normalise to exactly 1
    return np.interp(x, g, cdf_vals, left=0.0, right=1.0)


def _edge_margin(beta: float, m: int, n: int, trim_kind: str,
                 c_tw: float, frac_sq: float, frac_rect: float) -> float:
    """
    Compute the edge trimming margin δ for defining the interior MP bulk.

    Three strategies are supported:

    * ``"tw"`` — Tracy-Widom fluctuation scale:
      ``δ = c_tw · n_eff^{−2/3} · (1 + √β)^{4/3}``
    * ``"fraction"`` — Fixed fraction of bulk bandwidth (b − a):
      ``δ = frac_sq·(b−a)`` for square matrices, ``frac_rect·(b−a)`` otherwise.
    * ``"tw_or_fraction"`` — ``max(tw_margin, fraction_margin)`` (default).

    Parameters
    ----------
    beta : float
        Aspect ratio.
    m, n : int
        Matrix dimensions.
    trim_kind : str
        One of ``"tw"``, ``"fraction"``, ``"tw_or_fraction"``.
    c_tw : float
        Multiplicative constant for the TW margin.
    frac_sq : float
        Fractional trim for square matrices (m == n).
    frac_rect : float
        Fractional trim for rectangular matrices.

    Returns
    -------
    float
        Trimming margin δ ≥ 0.

    Raises
    ------
    ValueError
        If ``trim_kind`` is not recognised.
    """
    a, b      = _mp_support(beta)
    bandwidth = b - a
    n_eff     = min(m, n)
    is_square = (m == n)
    tw   = c_tw * (n_eff ** (-2/3)) * (1 + np.sqrt(beta))**(4/3)
    frac = (frac_sq if is_square else frac_rect) * bandwidth
    if trim_kind == "tw":             return tw
    if trim_kind == "fraction":       return frac
    if trim_kind == "tw_or_fraction": return max(tw, frac)
    raise ValueError("Invalid trim_kind")


def _find_manifest_entry(manifest: dict, target: str) -> dict:
    """
    Locate a matrix entry in the W_MP manifest by name or path fragment.

    Performs exact name match first, then falls back to substring search on
    both ``"name"`` and ``"path"`` fields.

    Parameters
    ----------
    manifest : dict
        Loaded manifest JSON (must have a ``"files"`` list).
    target : str
        Full or partial matrix name.

    Returns
    -------
    dict
        The matching manifest entry.

    Raises
    ------
    ValueError
        If no entry matches ``target``.
    """
    for e in manifest["files"]:
        if e.get("name", "") == target:
            return e
    for e in manifest["files"]:
        if target in e.get("name", "") or target in e.get("path", ""):
            return e
    raise ValueError(f"Matrix '{target}' not found in manifest")


def compute_trimmed(W: np.ndarray, TRIM_KIND: str, C_TW: float,
                    EDGE_FRAC_SQUARE: float, EDGE_FRAC_RECT: float,
                    beta_override=None):
    """
    Compute the trimmed interior eigenvalue spectrum and conditional MP functions.

    Steps
    -----
    1. SVD of W; convert singular values to scaled eigenvalues
       λ_i = s_i² / max(m, n).
    2. Compute trim margin δ via ``_edge_margin``.
    3. Keep only λ ∈ [a + δ, b − δ].
    4. Build conditional MP PDF and CDF renormalised to [L, U].

    Parameters
    ----------
    W : np.ndarray, shape (m, n)
        Column-standardised weight matrix (W_MP).
    TRIM_KIND : str
        Edge trimming strategy; see ``_edge_margin``.
    C_TW : float
        TW constant multiplier.
    EDGE_FRAC_SQUARE : float
        Fractional trim for square matrices.
    EDGE_FRAC_RECT : float
        Fractional trim for rectangular matrices.
    beta_override : float or None
        If provided, use this β instead of min(m,n)/max(m,n).
        In ALBERT, always set to 0.25 (rectangular) or 1.00 (square).

    Returns
    -------
    lam_trim : np.ndarray
        Sorted interior eigenvalues surviving the trim.
    (a, b, L, U, beta) : tuple of float
        Full support edges, trim boundaries, and β used.
    mp_pdf_cond : callable
        Conditional MP PDF: ρ_MP(λ) / (F_MP(U) − F_MP(L)).
    mp_cdf_cond : callable
        Conditional MP CDF clipped to [0, 1].
    N_trim : int
        Number of eigenvalues surviving the trim.
    """
    m, n  = W.shape
    beta  = beta_override if beta_override is not None else min(m, n) / max(m, n)
    s     = np.linalg.svd(W, full_matrices=False, compute_uv=False)
    lambdas = (s**2) / max(m, n)
    lambdas.sort()

    a, b  = _mp_support(beta)
    delta = _edge_margin(beta, m, n, TRIM_KIND, C_TW,
                         EDGE_FRAC_SQUARE, EDGE_FRAC_RECT)
    L, U  = a + delta, b - delta

    # Fall back to full support if the margin collapses the interval
    if L >= U:
        L, U = a, b

    mask     = (lambdas >= L) & (lambdas <= U)
    lam_trim = lambdas[mask]
    N_trim   = lam_trim.size

    FL, FU = _mp_cdf([L, U], beta)
    den    = max(FU - FL, 1e-12)

    mp_pdf_cond = lambda x: _mp_pdf(x, beta, a, b) / den
    mp_cdf_cond = lambda x: np.clip((_mp_cdf(x, beta) - FL) / den, 0, 1)

    return lam_trim, (a, b, L, U, beta), mp_pdf_cond, mp_cdf_cond, N_trim


