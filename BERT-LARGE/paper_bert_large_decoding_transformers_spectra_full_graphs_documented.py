# SECTION 1 — EXTRACT WEIGHT MATRICES FROM BERT-LARGE
# =============================================================================

from pathlib import Path
import json
import numpy as np
import torch
from transformers import BertModel


def extract_matrices(
    model_name: str = "bert-large-uncased",
    out_dir: str = "bert_large_weights",
    include_bias: bool = False,
    only_linear_like: bool = True,
    dtype: str = "float32",
    save_format: str = "npy",
):
    """
    Download BERT-Large from HuggingFace and save all 2-D weight matrices.

    For each qualifying tensor in the model's ``state_dict`` the function
    converts it to a NumPy array, writes it to a mirrored directory tree
    inside ``out_dir``, and appends an entry to ``manifest.json`` that
    records the original parameter name, relative file path, shape, and
    dtype.  Embedding matrices (``model.embeddings.*``) are saved
    separately under ``out_dir/embeddings/``.

    Parameters
    ----------
    model_name : str
        HuggingFace model identifier, e.g. ``"bert-large-uncased"``.
    out_dir : str
        Root directory for saved weights and the manifest.
    include_bias : bool
        If ``True``, 1-D bias vectors are also saved (default: ``False``).
    only_linear_like : bool
        If ``True`` (default), keep only tensors whose parameter name
        contains one of the recognised linear-layer name fragments
        (``"encoder.layer"``, ``"attention"``, ``"dense"``, etc.).
    dtype : str
        NumPy dtype for saved arrays, either ``"float32"`` or ``"float64"``.
    save_format : str
        ``"npy"`` (default) for plain NumPy arrays or ``"npz"`` for
        compressed archives (key ``"data"``).

    Side effects
    ------------
    Creates ``out_dir`` (and sub-directories) if they do not exist.
    Writes one array file per qualifying parameter plus
    ``out_dir/manifest.json``.

    Prints
    ------
    The resolved output path, the total number of saved tensors, and a
    formatted table of all saved matrix names and shapes.
    """
    assert save_format in {"npy", "npz"}

    # Load model in inference mode on CPU to avoid unnecessary GPU allocation
    torch.set_grad_enabled(False)
    model = BertModel.from_pretrained(model_name)
    model.eval()
    model.to("cpu")

    out = Path(out_dir)
    out.mkdir(parents=True, exist_ok=True)

    sd = model.state_dict()

    # Manifest accumulates metadata for every saved tensor
    manifest = {
        "model_name": model_name,
        "dtype": dtype,
        "include_bias": include_bias,
        "only_linear_like": only_linear_like,
        "files": []
    }

    def keep_param(key: str, tensor: torch.Tensor) -> bool:
        """
        Return True if this parameter should be saved.

        Accepts 2-D tensors that match recognised linear-layer name fragments
        when ``only_linear_like`` is True.  Optionally accepts 1-D bias
        vectors when ``include_bias`` is True.
        """
        if tensor.ndim == 2:
            if only_linear_like:
                names_we_like = (
                    "encoder.layer", "attention", "intermediate",
                    "output.dense", "self.query", "self.key",
                    "self.value", "dense", "pooler.dense"
                )
                return any(n in key for n in names_we_like)
            return True
        if include_bias and tensor.ndim == 1:
            return "bias" in key
        return False

    # ---- Save qualifying tensors ----
    for key in sorted(sd.keys()):
        t = sd[key]
        if not keep_param(key, t):
            continue

        arr = t.detach().cpu().to(
            dtype=torch.float32 if dtype == "float32" else torch.float64
        ).numpy()

        # Convert dotted parameter key to a nested directory path.
        # Example: "encoder.layer.0.attention.self.query.weight"
        #       -> encoder/layer_0/attention/self/query/weight.npy
        parts = key.split(".")
        norm_parts = []
        for p in parts:
            if p == "layer":
                continue          # "layer" is implicit in the directory name
            if p.isdigit():
                norm_parts.append(f"layer_{p}")
            else:
                norm_parts.append(p)

        save_dir = out.joinpath(*norm_parts[:-1])
        save_dir.mkdir(parents=True, exist_ok=True)

        stem = norm_parts[-1]
        ext  = ".npy" if save_format == "npy" else ".npz"
        path = save_dir / f"{stem}{ext}"

        if save_format == "npy":
            np.save(path, arr)
        else:
            np.savez_compressed(path, data=arr)

        manifest["files"].append({
            "name":  key,
            "path":  str(path.relative_to(out)),
            "shape": list(arr.shape),
            "ndim":  arr.ndim,
            "dtype": str(arr.dtype)
        })

    # ---- Save embedding matrices separately ----
    emb_dir = out / "embeddings"
    emb_dir.mkdir(exist_ok=True)
    for subkey, param in model.embeddings.state_dict().items():
        if param.ndim == 2 or (include_bias and param.ndim == 1):
            arr = param.detach().cpu().to(
                dtype=torch.float32 if dtype == "float32" else torch.float64
            ).numpy()
            fname = (
                subkey.replace(".", "_")
                + ("_bias" if subkey.endswith("bias") else "")
                + (".npy" if save_format == "npy" else ".npz")
            )
            path = emb_dir / fname
            if save_format == "npy":
                np.save(path, arr)
            else:
                np.savez_compressed(path, data=arr)

            manifest["files"].append({
                "name":  f"embeddings.{subkey}",
                "path":  str(path.relative_to(out)),
                "shape": list(arr.shape),
                "ndim":  arr.ndim,
                "dtype": str(arr.dtype)
            })

    # ---- Write manifest ----
    with open(out / "manifest.json", "w") as f:
        json.dump(manifest, f, indent=2)

    print(f"Saved to: {out.resolve()}")
    print(f"Total tensors saved: {len(manifest['files'])}")
    print("\nAvailable matrices:")
    for e in manifest["files"]:
        print(f"  {e['name']:70s}  shape={e['shape']}")


if __name__ == "__main__":
    extract_matrices(
        model_name="bert-large-uncased",
        out_dir="bert_large_weights",
        include_bias=False,
        only_linear_like=True,
        dtype="float32",
        save_format="npy",
    )


