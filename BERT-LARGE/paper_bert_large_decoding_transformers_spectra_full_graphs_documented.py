# extract_albert_matrices.py
from pathlib import Path
import json
import numpy as np
import torch
from transformers import AlbertModel


def extract_matrices(
    model_name: str = "albert-base-v2",
    out_dir: str = "albert_weights",
    include_bias: bool = False,
    only_linear_like: bool = True,
    dtype: str = "float32",
    save_format: str = "npy",
):
    """
    Download ALBERT from HuggingFace and save all qualifying 2-D weight matrices.

    ALBERT uses cross-layer parameter sharing, so only one copy of each matrix
    type exists (stored under ``encoder.albert_layer_groups.0.albert_layers.0.*``).
    Numeric indices in parameter keys are prefixed with ``"g"`` in the saved
    directory paths (e.g. ``g0`` for group 0, layer 0) to produce valid
    filesystem names.

    Parameters
    ----------
    model_name : str
        HuggingFace model identifier, e.g. ``"albert-base-v2"``.
    out_dir : str
        Root directory for saved weights and the manifest.
    include_bias : bool
        If ``True``, 1-D bias vectors are also saved (default: ``False``).
    only_linear_like : bool
        If ``True`` (default), keep only tensors whose parameter name contains
        one of the recognised linear-layer fragments:
        ``"attention.query"``, ``"attention.key"``, ``"attention.value"``,
        ``"attention.dense"``, ``"ffn."``, ``"ffn_output"``,
        ``"full_layer_layer_norm"``, ``"pooler.dense"``,
        ``"encoder.embedding_hidden_mapping_in"``.
    dtype : str
        NumPy dtype for saved arrays: ``"float32"`` (default) or ``"float64"``.
    save_format : str
        ``"npy"`` (default) for plain arrays or ``"npz"`` for compressed archives
        (key ``"data"``).

    Side effects
    ------------
    Creates ``out_dir`` (and sub-directories) if they do not exist.
    Writes one array file per qualifying parameter plus
    ``out_dir/manifest.json``.

    Prints
    ------
    The resolved output path, the total number of saved tensors, and a
    formatted table of all saved matrix names and shapes.
    """
    assert save_format in {"npy", "npz"}

    # Load model in inference mode on CPU
    torch.set_grad_enabled(False)
    model = AlbertModel.from_pretrained(model_name)
    model.eval()
    model.to("cpu")

    out = Path(out_dir)
    out.mkdir(parents=True, exist_ok=True)

    sd = model.state_dict()
    manifest = {
        "model_name": model_name,
        "dtype": dtype,
        "include_bias": include_bias,
        "only_linear_like": only_linear_like,
        "files": []
    }

    def keep_param(key: str, tensor: torch.Tensor) -> bool:
        """
        Return True if this parameter should be saved.

        Accepts 2-D tensors matching recognised ALBERT linear-layer name
        fragments when ``only_linear_like`` is True.  Optionally includes
        1-D bias vectors when ``include_bias`` is True.
        """
        if tensor.ndim == 2:
            if only_linear_like:
                names_we_like = (
                    "attention.query", "attention.key", "attention.value",
                    "attention.dense", "ffn.", "ffn_output",
                    "full_layer_layer_norm", "pooler.dense",
                    "encoder.embedding_hidden_mapping_in",
                )
                return any(n in key for n in names_we_like)
            return True
        if include_bias and tensor.ndim == 1:
            return "bias" in key
        return False

    # ---- Save qualifying tensors ----
    for key in sorted(sd.keys()):
        t = sd[key]
        if not keep_param(key, t):
            continue

        arr = t.detach().cpu().to(
            dtype=torch.float32 if dtype == "float32" else torch.float64
        ).numpy()

        # Convert dotted parameter key to a nested directory path.
        # Example: "encoder.albert_layer_groups.0.albert_layers.0.attention.query.weight"
        #       -> encoder/albert_layer_groups/g0/albert_layers/g0/attention/query/weight.npy
        # Numeric indices are prefixed with "g" to create valid directory names.
        parts = key.split(".")
        norm_parts = []
        for p in parts:
            if p.isdigit():
                norm_parts.append(f"g{p}")   # group/layer index -> g0, g1, ...
            else:
                norm_parts.append(p)

        save_dir = out.joinpath(*norm_parts[:-1])
        save_dir.mkdir(parents=True, exist_ok=True)

        stem = norm_parts[-1]
        ext  = ".npy" if save_format == "npy" else ".npz"
        path = save_dir / f"{stem}{ext}"

        if save_format == "npy":
            np.save(path, arr)
        else:
            np.savez_compressed(path, data=arr)

        manifest["files"].append({
            "name":  key,
            "path":  str(path.relative_to(out)),
            "shape": list(arr.shape),
            "ndim":  arr.ndim,
            "dtype": str(arr.dtype)
        })

    # ---- Save embedding matrices separately ----
    emb_dir = out / "embeddings"
    emb_dir.mkdir(exist_ok=True)
    for subkey, param in model.embeddings.state_dict().items():
        if param.ndim == 2 or (include_bias and param.ndim == 1):
            arr = param.detach().cpu().to(
                dtype=torch.float32 if dtype == "float32" else torch.float64
            ).numpy()
            fname = (
                subkey.replace(".", "_")
                + ("_bias" if subkey.endswith("bias") else "")
                + (".npy" if save_format == "npy" else ".npz")
            )
            path = emb_dir / fname
            if save_format == "npy":
                np.save(path, arr)
            else:
                np.savez_compressed(path, data=arr)

            manifest["files"].append({
                "name":  f"embeddings.{subkey}",
                "path":  str(path.relative_to(out)),
                "shape": list(arr.shape),
                "ndim":  arr.ndim,
                "dtype": str(arr.dtype)
            })

    # ---- Write manifest ----
    with open(out / "manifest.json", "w") as f:
        json.dump(manifest, f, indent=2)

    print(f"Saved to: {out.resolve()}")
    print(f"Total tensors saved: {len(manifest['files'])}")

    # Print available matrix names — useful for configuring graph SETTINGS
    print("\nAvailable matrices:")
    for e in manifest["files"]:
        print(f"  {e['name']:70s}  shape={e['shape']}")


if __name__ == "__main__":
    extract_matrices(
        model_name="albert-base-v2",
        out_dir="albert_weights",
        include_bias=False,
        only_linear_like=True,
        dtype="float32",
        save_format="npy",
    )


