# ---- Scenario functions ----
# Each function(layer, method, alpha) -> float in [0, 1] representing the
# probability that a KS test *accepts* H₀ for that (layer, method, alpha).

def scenario_baseline(layer, method, alpha):
    """Constant acceptance probability: KS-strict ≈ 55 %, KS-TW ≈ 65 %."""
    return 0.55 if method == "KS-strict" else 0.65

def scenario_strict_dominance(layer, method, alpha):
    """KS-strict outperforms KS-TW (higher acceptance rate)."""
    return 0.75 if method == "KS-strict" else 0.90

def scenario_low_alpha(layer, method, alpha):
    """Strong α-dependence: very few acceptances at α=0.01."""
    if alpha == 0.01:
        return 0.15 if method == "KS-strict" else 0.35
    elif alpha == 0.05:
        return 0.45 if method == "KS-strict" else 0.55
    else:
        return 0.75 if method == "KS-strict" else 0.80

def scenario_square_vs_rect(layer, method, alpha):
    """
    Square attention matrices (layers 0-11) are well-fitted;
    rectangular FFN matrices (layers 12-23) are poorly fitted.
    """
    if layer < 12:
        return 0.80 if method == "KS-strict" else 0.90
    else:
        return 0.30 if method == "KS-strict" else 0.50

def scenario_edge_sensitive(layer, method, alpha):
    """
    Alternating pattern by layer index, suggesting sensitivity to spectral
    edge effects.  KS-TW is consistently more accepting.
    """
    if method == "KS-strict":
        return 0.40 + 0.20 * (layer % 2)
    else:
        return 0.70

def scenario_extreme_disagreement(layer, method, alpha):
    """
    Extreme case: KS-strict almost always rejects, KS-TW almost always accepts.
    Illustrates how edge trimming can qualitatively change the conclusion.
    """
    return 0.12 if method == "KS-strict" else 0.87

