def _check_one(relpath: str, mu: np.ndarray, sd: np.ndarray) -> float:
    """
    Verify that W can be exactly recovered from W_MP, μ, and σ.

    Computes the max absolute reconstruction error::

        max |W − (W_MP · σ + μ)|

    Parameters
    ----------
    relpath : str
        Relative path to the original weight file inside ``WEIGHTS_DIR``.
    mu : np.ndarray
        Per-column mean vector (shape ``(n,)``).
    sd : np.ndarray
        Per-column standard deviation vector (shape ``(n,)``).

    Returns
    -------
    float
        Maximum absolute reconstruction error.
    """
    W    = _load_matrix(weights_dir / relpath).astype(np.float64)
    WMP  = _load_matrix(out_root  / relpath).astype(np.float64)
    safe_sd = np.where(sd == 0, 1.0, sd)
    W_rec   = WMP * safe_sd.reshape(1, -1) + mu.reshape(1, -1)
    return float(np.max(np.abs(W - W_rec)))


