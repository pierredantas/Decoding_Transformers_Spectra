"""
The observed eCDF (dark line) is compared against a pointwise 90 % bootstrap
envelope (shaded band) generated by drawing n=500 samples from the null
distribution.  Scenarios cover various departures from the null (shifted mean,
heavy tails, compressed variance, mixture, small sample).

Output: graph_shrinkage_control_04_albert.pdf  (1×6 subplot grid, 7×1 in)
"""

import numpy as np
import matplotlib.pyplot as plt
from matplotlib.lines import Line2D
from matplotlib.patches import Patch

plt.rcParams.update({
    "font.family": "serif",
    "font.serif": ["Times New Roman", "Times", "DejaVu Serif"],
    "mathtext.fontset": "stix",
    "font.size": 7, "axes.titlesize": 7, "axes.labelsize": 5,
    "xtick.labelsize": 5, "ytick.labelsize": 5, "legend.fontsize": 5,
    "axes.linewidth": 0.6, "lines.linewidth": 1.2,
    "grid.linewidth": 0.4, "grid.alpha": 0.3,
    "pdf.fonttype": 42, "ps.fonttype": 42,
})

np.random.seed(42)


def ecdf(x: np.ndarray, grid: np.ndarray) -> np.ndarray:
    """
    Evaluate the empirical CDF of ``x`` at the points in ``grid``.

    Parameters
    ----------
    x : np.ndarray
        Observed data.
    grid : np.ndarray
        Evaluation points.

    Returns
    -------
    np.ndarray
        eCDF values in [0, 1], same shape as ``grid``.
    """
    return np.searchsorted(np.sort(x), grid, side="right") / len(x)


def generate_data(kind: str, n: int) -> np.ndarray:
    """
    Sample ``n`` observations from a named distribution.

    Parameters
    ----------
    kind : str
        One of ``"normal"``, ``"shifted"``, ``"t3"``, ``"compressed"``,
        ``"mixture"``, ``"chisq"`` / ``"chisquare"``.
    n : int
        Number of samples.

    Returns
    -------
    np.ndarray, shape (n,)
    """
    if kind == "normal":
        return np.random.normal(size=n)
    elif kind == "shifted":
        return np.random.normal(loc=1.0, scale=1.0, size=n)
    elif kind == "t3":
        return np.random.standard_t(df=3, size=n)
    elif kind == "compressed":
        return np.random.normal(loc=0.0, scale=0.5, size=n)
    elif kind == "mixture":
        return np.concatenate([
            np.random.normal(loc=0.0, scale=1.0, size=n//2),
            np.random.normal(loc=2.0, scale=1.0, size=n//2)
        ])
    else:   # "chisq"
        return np.random.chisquare(df=3, size=n) / 3.0


# Each tuple: (observed dist., null dist., n, title, (x_min, x_max))
scenarios = [
    ("chisq",      "chisq",  500, "Baseline ($\\chi^2$ null)", (0,  4)),
    ("shifted",    "normal", 500, "Shifted mean",              (-2, 4)),
    ("t3",         "normal", 500, "Heavy-tailed ($t_3$)",      (-4, 4)),
    ("compressed", "normal", 500, "Compressed variance",       (-2, 2)),
    ("mixture",    "normal", 500, "Mixture (0 & 2)",           (-1, 4)),
    ("chisq",      "chisq",   50, "Small $n=50$",              (0,  4)),
]

print("Building mosaic figure...")

fig, axes = plt.subplots(1, 6, figsize=(7, 1), sharex=False, sharey=True)
axes = axes.flatten()

for i, (ax, (obs_kind, boot_kind, n, title, (L, U))) in enumerate(
        zip(axes, scenarios)):
    print(f"  [{i+1}/6] {title}")

    observed  = generate_data(obs_kind, n)
    grid      = np.linspace(L, U, 300)
    obs_ecdf  = ecdf(observed, grid)

    # 300-replicate bootstrap of the null eCDF → pointwise 90 % envelope
    boot_ecdfs = np.array([ecdf(generate_data(boot_kind, n), grid)
                           for _ in range(300)])
    lower = np.percentile(boot_ecdfs, 5,  axis=0)
    upper = np.percentile(boot_ecdfs, 95, axis=0)

    ax.fill_between(grid, lower, upper, color="#cccccc", alpha=0.6)
    ax.plot(grid, obs_ecdf, color="#333333", linewidth=0.8)

    ax.set_xlim(L, U)
    ax.set_ylim(0, 1)
    ax.set_title(title, pad=2)
    ax.grid(True)

    if i != 0:
        ax.yaxis.set_ticklabels([])

handles = [
    Line2D([0], [0], color="#333333", linewidth=0.8, label="eCDF"),
    Patch(facecolor="#cccccc", alpha=0.6, label="Bootstrap"),
]
axes[0].legend(handles=handles, loc="upper left", frameon=False)

fig.text(0, 0.5, "$x$ vs. eCDF", va="center",
         rotation="vertical", fontsize=6)

