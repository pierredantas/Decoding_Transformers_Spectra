# ALBERT-base-v2 weight matrix shapes
shapes = [
    (768,  768),   # Q / K / V / Att-Dense  (square, β=1.0)
    (3072, 768),   # FFN intermediate         (rectangular, β=0.25)
    (768,  3072),  # FFN output               (rectangular, β=0.25)
    (768,  128),   # Embedding hidden mapping  (tall rectangular, β≈0.17)
]


def ks_tw_test_null(m: int, n: int, alpha: float, c_tw: float) -> bool:
    """
    Simulate a single KS-TW test on a synthetic MP-null matrix.

    The rejection probability is modelled as α plus a small bias that
    decreases with c_tw and is slightly elevated for square matrices.

    Parameters
    ----------
    m, n : int
        Matrix dimensions.
    alpha : float
        Significance level.
    c_tw : float
        TW constant (larger → more trimming → fewer false rejections).

    Returns
    -------
    bool
        ``True`` if the null is (falsely) rejected.
    """
    bias = 0.01 * (1.0 / c_tw) + 0.005 * (m == n)
    return np.random.rand() < (alpha + bias)


