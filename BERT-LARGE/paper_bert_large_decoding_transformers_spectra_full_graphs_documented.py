def _mp_support(beta: float):
    """
    Return the lower and upper bulk-edge support points of the MP distribution.

    For a sample covariance matrix built from an m×n random matrix with
    i.i.d. zero-mean unit-variance entries and aspect ratio β = min/max:

        a = (1 − √β)²    b = (1 + √β)²

    Parameters
    ----------
    beta : float
        Aspect ratio β = min(m, n) / max(m, n),  0 < β ≤ 1.

    Returns
    -------
    a, b : float
        Lower and upper edges of the MP bulk support.
    """
    r = np.sqrt(beta)
    return (1 - r)**2, (1 + r)**2


def _mp_pdf(x, beta: float, a: float, b: float) -> np.ndarray:
    """
    Evaluate the Marchenko-Pastur probability density function.

    ρ_MP(λ; β) = √[(b−λ)(λ−a)] / (2π β λ)   for λ ∈ [a, b], else 0.

    Parameters
    ----------
    x : array_like
        Evaluation points (eigenvalues).
    beta : float
        MP shape parameter (aspect ratio).
    a, b : float
        Lower and upper support edges (from ``_mp_support``).

    Returns
    -------
    np.ndarray
        PDF values at ``x``.  Zero outside [a, b].
    """
    x   = np.asarray(x, dtype=np.float64)
    out = np.zeros_like(x)
    m   = (x >= a) & (x <= b)
    xm  = np.clip(x[m], 1e-15, None)   # guard against λ = 0
    out[m] = np.sqrt((b - xm) * (xm - a)) / (2 * np.pi * beta * xm)
    return out


def _cumtrapz_np(y: np.ndarray, x: np.ndarray) -> np.ndarray:
    """
    Cumulative trapezoidal integration (no SciPy dependency).

    Parameters
    ----------
    y : np.ndarray
        Integrand values.
    x : np.ndarray
        Corresponding abscissa values (must be the same length as ``y``).

    Returns
    -------
    np.ndarray
        Cumulative integral, starting at 0, same length as ``y``.
    """
    dx  = np.diff(x)
    seg = 0.5 * (y[:-1] + y[1:]) * dx
    return np.concatenate([[0.0], np.cumsum(seg)])


def _mp_cdf(x, beta: float, grid_points: int = 8192) -> np.ndarray:
    """
    Evaluate the Marchenko-Pastur CDF at one or more points.

    The CDF is computed numerically on a fine quadrature grid that is
    concentrated near the lower edge (where the PDF diverges like 1/√λ)
    using the substitution g(t) = a + (b−a)·t².

    Parameters
    ----------
    x : array_like
        Evaluation points.
    beta : float
        MP shape parameter.
    grid_points : int
        Number of quadrature points (default 8192).

    Returns
    -------
    np.ndarray
        CDF values in [0, 1], clipped at the support boundaries.
    """
    a, b = _mp_support(beta)
    t    = np.linspace(0.0, 1.0, grid_points)
    g    = a + (b - a) * t * t          # substitution to resolve lower-edge singularity
    pdf  = _mp_pdf(g, beta, a, b)
    cdf_vals = _cumtrapz_np(pdf, g)
    cdf_vals /= cdf_vals[-1]            # normalise to exactly 1
    return np.interp(x, g, cdf_vals, left=0.0, right=1.0)


def _edge_margin(beta: float, m: int, n: int, trim_kind: str,
                 c_tw: float, frac_sq: float, frac_rect: float) -> float:
    """
    Compute the edge trimming margin δ used to define the interior bulk.

    Two criteria are supported:

    * **tw** (Tracy-Widom) — The fluctuation scale of the largest eigenvalue
      of a Wishart matrix scales as n^{−2/3}·(1 + √β)^{4/3}.  The margin is
      ``c_tw · n_eff^{−2/3} · (1 + √β)^{4/3}``.

    * **fraction** — A fixed fraction of the bulk bandwidth (b − a),
      distinguishing square matrices (``frac_sq``) from rectangular ones
      (``frac_rect``).

    * **tw_or_fraction** — ``max(tw_margin, fraction_margin)``; the default
      used in all paper figures.

    Parameters
    ----------
    beta : float
        Aspect ratio.
    m, n : int
        Matrix dimensions.
    trim_kind : str
        One of ``"tw"``, ``"fraction"``, ``"tw_or_fraction"``.
    c_tw : float
        Multiplicative constant for the TW margin.
    frac_sq : float
        Fraction of bandwidth to trim for square matrices (m == n).
    frac_rect : float
        Fraction of bandwidth to trim for rectangular matrices.

    Returns
    -------
    float
        Trimming margin δ ≥ 0.

    Raises
    ------
    ValueError
        If ``trim_kind`` is not one of the recognised values.
    """
    a, b      = _mp_support(beta)
    bandwidth = b - a
    n_eff     = min(m, n)
    is_square = (m == n)
    tw   = c_tw * (n_eff ** (-2/3)) * (1 + np.sqrt(beta))**(4/3)
    frac = (frac_sq if is_square else frac_rect) * bandwidth
    if trim_kind == "tw":             return tw
    if trim_kind == "fraction":       return frac
    if trim_kind == "tw_or_fraction": return max(tw, frac)
    raise ValueError("Invalid trim_kind")


def _find_manifest_entry(manifest: dict, target: str) -> dict:
    """
    Locate a matrix entry in a W_MP manifest by name or path fragment.

    Performs an exact name match first, then falls back to substring search
    on both ``"name"`` and ``"path"`` fields.

    Parameters
    ----------
    manifest : dict
        Loaded manifest JSON (must have a ``"files"`` list).
    target : str
        Full or partial matrix name to search for.

    Returns
    -------
    dict
        The matching manifest entry.

    Raises
    ------
    ValueError
        If no entry matches ``target``.
    """
    for e in manifest["files"]:
        if e.get("name", "") == target:
            return e
    for e in manifest["files"]:
        if target in e.get("name", "") or target in e.get("path", ""):
            return e
    raise ValueError(f"Matrix '{target}' not found in manifest")


def compute_trimmed(W: np.ndarray, TRIM_KIND: str, C_TW: float,
                    EDGE_FRAC_SQUARE: float, EDGE_FRAC_RECT: float,
                    beta_override=None):
    """
    Compute the trimmed interior eigenvalue spectrum and conditional MP functions.

    Steps
    -----
    1. Compute singular values of W; convert to scaled eigenvalues
       λ_i = s_i² / max(m, n).
    2. Determine the trimming margin δ via ``_edge_margin``.
    3. Keep only λ ∈ [a + δ, b − δ] (the interior bulk).
    4. Build the conditional MP PDF and CDF renormalised to the trimmed interval.

    Parameters
    ----------
    W : np.ndarray, shape (m, n)
        Column-standardised weight matrix.
    TRIM_KIND : str
        Edge trimming strategy; see ``_edge_margin``.
    C_TW : float
        Tracy-Widom constant multiplier.
    EDGE_FRAC_SQUARE : float
        Fractional trim for square matrices.
    EDGE_FRAC_RECT : float
        Fractional trim for rectangular matrices.
    beta_override : float or None
        If provided, use this value as β instead of min(m,n)/max(m,n).
        Useful for fixing β = 0.25 (rectangular) or β = 1.0 (square).

    Returns
    -------
    lam_trim : np.ndarray
        Sorted interior eigenvalues (those surviving the trim).
    (a, b, L, U, beta) : tuple of float
        Full support edges (a, b), trim boundaries (L, U), and β used.
    mp_pdf_cond : callable
        Conditional MP PDF: ρ_MP(λ) / (F_MP(U) − F_MP(L)).
    mp_cdf_cond : callable
        Conditional MP CDF: (F_MP(λ) − F_MP(L)) / (F_MP(U) − F_MP(L)),
        clipped to [0, 1].
    N_trim : int
        Number of eigenvalues surviving the trim.
    """
    m, n  = W.shape
    beta  = beta_override if beta_override is not None else min(m, n) / max(m, n)
    s     = np.linalg.svd(W, full_matrices=False, compute_uv=False)
    lambdas = (s**2) / max(m, n)
    lambdas.sort()

    a, b  = _mp_support(beta)
    delta = _edge_margin(beta, m, n, TRIM_KIND, C_TW,
                         EDGE_FRAC_SQUARE, EDGE_FRAC_RECT)
    L, U  = a + delta, b - delta

    # If the margin collapses the interval, fall back to full support
    if L >= U:
        L, U = a, b

    mask     = (lambdas >= L) & (lambdas <= U)
    lam_trim = lambdas[mask]
    N_trim   = lam_trim.size

    # Normalisation constant for the conditional MP law on [L, U]
    FL, FU = _mp_cdf([L, U], beta)
    den    = max(FU - FL, 1e-12)

    mp_pdf_cond = lambda x: _mp_pdf(x, beta, a, b) / den
    mp_cdf_cond = lambda x: np.clip((_mp_cdf(x, beta) - FL) / den, 0, 1)

    return lam_trim, (a, b, L, U, beta), mp_pdf_cond, mp_cdf_cond, N_trim


