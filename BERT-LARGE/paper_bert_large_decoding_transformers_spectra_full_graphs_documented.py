def _safe_key(idx: int, kind: str, name: str) -> str:
    """
    Build a stable, filesystem-safe NPZ key for per-column statistics.

    The key has the form ``{idx:04d}__{kind}__{sanitised_name}`` where
    ``kind`` is ``"mean"`` or ``"std"``.  Non-alphanumeric characters in
    ``name`` are replaced with underscores, consecutive underscores are
    collapsed, and the result is capped at 200 characters.

    Parameters
    ----------
    idx : int
        Zero-based index of the matrix in the manifest.
    kind : str
        ``"mean"`` or ``"std"``.
    name : str
        Original HuggingFace parameter name.

    Returns
    -------
    str
        A ≤200-character NPZ-safe key string.
    """
    base = f"{idx:04d}__{kind}__" + re.sub(r"[^0-9a-zA-Z_]+", "_", name)
    base = re.sub(r"__+", "__", base).strip("_")
    return base[:200]


def _load_matrix(path: Path) -> np.ndarray:
    """
    Load a NumPy array from a ``.npy`` or ``.npz`` file.

    For ``.npz`` files the array is expected under the key ``"data"``.

    Parameters
    ----------
    path : Path
        Path to the file.

    Returns
    -------
    np.ndarray
        The stored array.

    Raises
    ------
    ValueError
        If the file extension is neither ``.npy`` nor ``.npz``.
    """
    if path.suffix == ".npy":
        return np.load(path)
    if path.suffix == ".npz":
        return np.load(path)["data"]
    raise ValueError(f"Unsupported file type: {path.suffix}")


def _save_npy(path: Path, arr: np.ndarray) -> None:
    """
    Save a NumPy array to ``path``, creating parent directories as needed.

    Parameters
    ----------
    path : Path
        Destination file path (should end in ``.npy``).
    arr : np.ndarray
        Array to save.
    """
    path.parent.mkdir(parents=True, exist_ok=True)
    np.save(path, arr)


