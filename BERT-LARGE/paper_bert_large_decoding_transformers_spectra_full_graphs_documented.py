# ---- Scenario functions: D_p = func(layer, c_TW) ----

def scenario_stable_accept(layer, c):
    """D_p uniformly low for all layers and c values: consistently accepted."""
    return np.random.uniform(0.05, 0.08)

def scenario_relax_accept(layer, c):
    """D_p decreases as c increases (larger trim margin → better fit)."""
    return 0.20 / c + np.random.uniform(-0.02, 0.02)

def scenario_persistent_reject(layer, c):
    """D_p uniformly high regardless of c: not MP-distributed."""
    return np.random.uniform(0.18, 0.25)

def scenario_mixed(layer, c):
    """
    Layer-dependent pattern: early layers (0-11) are sensitive to c,
    later layers (12-23) are robust with low D_p.
    """
    if layer < 12:
        return 0.20 / c
    else:
        return np.random.uniform(0.08, 0.10)

def scenario_edgesensitive(layer, c):
    """D_p oscillates with c following a sinusoidal pattern (edge artefact)."""
    return 0.12 + 0.05 * np.sin(0.5 * np.pi * c) \
                + np.random.uniform(-0.01, 0.01)

def scenario_alpha_dependent(layer, c):
    """D_p decreases with c; multiple thresholds shown for α ∈ {0.10, 0.12, 0.15}."""
    return 0.20 / c + np.random.uniform(-0.02, 0.02)

