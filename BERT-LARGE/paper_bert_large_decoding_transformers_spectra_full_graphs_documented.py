np.random.seed(42)
mat_types   = ["Q", "K", "V", "Att-Dense", "FFN", "FFN-Out"]
c_TW_values = [1, 2, 3]
thresholds  = [0.10, 0.12, 0.15]


# ---- Scenario functions: D_p = func(mat_index, c_TW) ----

def scenario_stable_accept(mat, c):
    """D_p uniformly low: all matrix types accepted regardless of c."""
    return np.random.uniform(0.05, 0.08)

def scenario_relax_accept(mat, c):
    """D_p decreases as c increases (larger trim → better interior fit)."""
    return 0.20 / c + np.random.uniform(-0.02, 0.02)

def scenario_persistent_reject(mat, c):
    """D_p uniformly high: none of the matrices follow the MP law."""
    return np.random.uniform(0.18, 0.25)

def scenario_mixed(mat, c):
    """
    Square matrices (Q/K/V/Att-Dense, indices 0-3) are c-sensitive;
    rectangular matrices (FFN/FFN-Out, indices 4-5) are robustly low.
    """
    if mat < 4:
        return 0.20 / c
    else:
        return np.random.uniform(0.08, 0.10)

def scenario_edgesensitive(mat, c):
    """D_p oscillates with c via a sinusoidal pattern (spectral edge artefact)."""
    return 0.12 + 0.05 * np.sin(0.5 * np.pi * c) \
                + np.random.uniform(-0.01, 0.01)

def scenario_alpha_dependent(mat, c):
    """D_p decreases with c; multiple α thresholds shown."""
    return 0.20 / c + np.random.uniform(-0.02, 0.02)

SCENARIOS = [
    ("Stable accept.",          scenario_stable_accept),
    ("Strict→Relax accept.",    scenario_relax_accept),
    ("Persistent rejection",    scenario_persistent_reject),
    ("Mixed families",          scenario_mixed),
    ("Edge-sensitive",          scenario_edgesensitive),
    ("$\\alpha$-dependent",     scenario_alpha_dependent),
]

