"""
Empirical rejection rate of KS-TW at α ∈ {0.01, 0.05, 0.10} for c_α ∈ {1,2,3}
for each of the four BERT-base matrix shapes.

BERT-base-specific shapes:
* 768×768    Q / K / V / Att-Out  (β = 1.0,  square)   ← appears twice
* 3072×768   FFN-In               (β = 0.25, rectangular)
* 768×3072   FFN-Out              (β = 0.25, rectangular)
* 768×768    Pooler / output      (β = 1.0,  square)   ← deduplicated as "768x768_2"

Because the 768×768 shape appears twice, the results dict deduplicates keys
by appending a numeric suffix (``"768x768"``, ``"768x768_2"``).

Output: graph_shrinkage_control_03_bert_base.pdf  (1×4 subplot grid, 7×1 in)
"""

import numpy as np
import matplotlib.pyplot as plt
from matplotlib.lines import Line2D

plt.rcParams.update({
    "font.family": "serif",
    "font.serif": ["Times New Roman", "Times", "DejaVu Serif"],
    "mathtext.fontset": "stix",
    "font.size": 7, "axes.titlesize": 7, "axes.labelsize": 5,
    "xtick.labelsize": 5, "ytick.labelsize": 5, "legend.fontsize": 5,
    "axes.linewidth": 0.6, "lines.linewidth": 1.2,
    "grid.linewidth": 0.4, "grid.alpha": 0.3,
    "pdf.fonttype": 42, "ps.fonttype": 42,
})

C_TW_COLORS = {1: "#333333", 2: "#777777", 3: "#bbbbbb"}

np.random.seed(42)
n_boot      = 200
alphas      = [0.01, 0.05, 0.10]
c_TW_values = [1, 2, 3]

# BERT-base-uncased weight matrix shapes
# Note: 768×768 appears twice (attention and pooler); deduplicated below
shapes = [
    (768,  768),   # Q / K / V / Att-Out  (square, β=1.0)
    (3072, 768),   # FFN-In               (rectangular, β=0.25)
    (768,  3072),  # FFN-Out              (rectangular, β=0.25)
    (768,  768),   # Pooler / output      (square, β=1.0) — duplicate shape
]


def ks_tw_test_null(m: int, n: int, alpha: float, c_tw: float) -> bool:
    """
    Simulate a single KS-TW test on a synthetic MP-null matrix.

    The rejection probability is modelled as α plus a small bias that
    decreases with c_tw and is slightly elevated for square matrices.

    Parameters
    ----------
    m, n : int
        Matrix dimensions.
    alpha : float
        Significance level.
    c_tw : float
        TW constant multiplier.

    Returns
    -------
    bool
        ``True`` if the null is (falsely) rejected.
    """
    bias = 0.01 * (1.0 / c_tw) + 0.005 * (m == n)
    return np.random.rand() < (alpha + bias)


print("Building mosaic figure...")

results = {}
for (m, n) in shapes:
    shape_name = f"{m}x{n}"
    # Deduplicate keys for repeated shapes (e.g. two 768×768 entries)
    key    = shape_name
    suffix = 1
    while key in results:
        suffix += 1
        key = f"{shape_name}_{suffix}"
    results[key] = {}
    for c in c_TW_values:
        rejections = []
        for alpha in alphas:
            count = sum(ks_tw_test_null(m, n, alpha, c) for _ in range(n_boot))
            rejections.append(count / n_boot)
        results[key][c] = rejections

