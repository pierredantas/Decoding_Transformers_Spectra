"""
Line plots of binned p-value histograms for KS-strict and KS-TW across six
simulation scenarios.  Under H₀ the p-value distribution should be uniform
on [0, 1]; deviations indicate anti-conservatism or conservatism.

Note: the scenario dict is named ``SCENARIOS_P`` here to avoid shadowing the
``SCENARIOS`` dict defined for Graph 7 in the same module scope.

Output: graph_shrinkage_control_01_bert_base.pdf  (1×6 subplot grid, 7×1 in)
"""

METHOD_COLORS = {"KS-strict": "#444444", "KS-TW": "#aaaaaa"}

n_boot = 1000


def simulate_pvalues(null: bool = True, skew: float = 0.0,
                     conservative: bool = False, seed=None) -> np.ndarray:
    """
    Generate simulated p-values for calibration diagnostics.

    Parameters
    ----------
    null : bool
        ``True`` → Uniform[0,1] (correct null).
        ``False`` → Beta(0.7, 1.0) (anti-conservative, excess small p-values).
    skew : float
        Right-skew via ``p → p^{1+skew}``.  Positive pushes mass toward 0.
    conservative : bool
        ``True`` → ``p → 1 − (1−p)²`` pushes mass toward 1.
    seed : int or None
        RNG seed for reproducibility.

    Returns
    -------
    np.ndarray, shape (n_boot,)
        Simulated p-values clipped to [0, 1].
    """
    rng = np.random.default_rng(seed)
    if null:
        pvals = rng.random(n_boot)
    else:
        pvals = rng.beta(0.7, 1.0, size=n_boot)
    if skew > 0:
        pvals = np.power(pvals, 1 + skew)
    if conservative:
        pvals = 1 - np.power(1 - pvals, 2)
    return np.clip(pvals, 0, 1)


