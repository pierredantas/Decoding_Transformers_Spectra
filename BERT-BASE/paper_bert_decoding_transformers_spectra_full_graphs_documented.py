# extract_bert_matrices.py
from pathlib import Path
import json
import numpy as np
import torch
from transformers import BertModel


def extract_matrices(
    model_name: str = "bert-base-uncased",
    out_dir: str = "bert_weights",
    include_bias: bool = False,
    only_linear_like: bool = True,      # skip LayerNorm/embeddings unless 2-D
    dtype: str = "float32",             # "float32" | "float64"
    save_format: str = "npy",           # "npy" | "npz" (np.savez_compressed)
):
    """
    Download BERT-base from HuggingFace and save all qualifying 2-D weight matrices.

    Iterates the full ``state_dict`` for robustness (covers all submodules),
    converts qualifying tensors to NumPy arrays, and writes them to a mirrored
    directory tree inside ``out_dir``.  A JSON manifest records the original
    parameter name, relative file path, shape, and dtype for every saved array.
    Embedding matrices are additionally saved under ``out_dir/embeddings/``.

    Key path mapping
    ----------------
    ``"encoder.layer.N.attention.self.query.weight"``
    → ``out_dir/encoder/layer_N/attention/self/query/weight.npy``

    The token ``"layer"`` is dropped and the following integer N is prefixed
    with ``"layer_"`` to produce valid directory names.

    Parameters
    ----------
    model_name : str
        HuggingFace model identifier, default ``"bert-base-uncased"``.
    out_dir : str
        Root directory for saved weights and the manifest.
    include_bias : bool
        If ``True``, 1-D bias vectors (matched by ``"bias"`` substring) are
        also saved (default: ``False``).
    only_linear_like : bool
        If ``True`` (default), keep only 2-D tensors whose parameter name
        contains one of the recognised linear-layer fragments:
        ``"encoder.layer"``, ``"attention"``, ``"intermediate"``,
        ``"output.dense"``, ``"self.query"``, ``"self.key"``,
        ``"self.value"``, ``"dense"``, ``"pooler.dense"``.
    dtype : str
        NumPy dtype for saved arrays: ``"float32"`` (default) or ``"float64"``.
    save_format : str
        ``"npy"`` (default) or ``"npz"`` (compressed, key ``"data"``).

    Side effects
    ------------
    Creates ``out_dir`` and sub-directories as needed.
    Writes one array file per qualifying parameter plus
    ``out_dir/manifest.json``.

    Prints
    ------
    Resolved output path and total number of saved tensors.
    """
    assert save_format in {"npy", "npz"}
    np_dtype = np.float32 if dtype == "float32" else np.float64  # noqa: F841

    # Load model in inference mode on CPU to avoid unnecessary GPU allocation
    torch.set_grad_enabled(False)
    model = BertModel.from_pretrained(model_name)
    model.eval()
    model.to("cpu")

    out = Path(out_dir)
    out.mkdir(parents=True, exist_ok=True)

    # Iterate state_dict for robustness — covers all submodules uniformly
    sd = model.state_dict()
    manifest = {
        "model_name": model_name,
        "dtype": dtype,
        "include_bias": include_bias,
        "only_linear_like": only_linear_like,
        "files": []
    }

    def keep_param(key: str, tensor: torch.Tensor) -> bool:
        """
        Return True if this parameter should be saved.

        Accepts 2-D tensors matching recognised linear/attention/FFN name
        fragments when ``only_linear_like`` is True.  Optionally includes
        1-D bias vectors when ``include_bias`` is True.
        """
        # Want weights (2-D) from linear/attention/FFN; optionally biases (1-D)
        if tensor.ndim == 2:
            if only_linear_like:
                # Heuristics: keep common linear/attention/FFN matrices
                names_we_like = (
                    "encoder.layer", "attention", "intermediate", "output.dense",
                    "self.query", "self.key", "self.value", "dense", "pooler.dense"
                )
                return any(n in key for n in names_we_like)
            return True

        if include_bias and tensor.ndim == 1:
            names_we_like = ("bias",)
            return any(n in key for n in names_we_like)

        return False

    # ---- Save qualifying tensors ----
    for key in sorted(sd.keys()):
        t = sd[key]
        if not keep_param(key, t):
            continue

        arr = t.detach().cpu().to(
            dtype=torch.float32 if dtype == "float32" else torch.float64
        ).numpy()

        # Build a safe, hierarchical path from the dotted parameter key.
        # Example: "encoder.layer.0.attention.self.query.weight"
        #       -> encoder/layer_0/attention/self/query/weight.npy
        parts = key.split(".")
        # Normalize "layer.N" to "layer_N"
        norm_parts = []
        for p in parts:
            if p == "layer":
                continue              # "layer" is implicit in the directory name
            if p.isdigit():           # the index that follows "layer"
                norm_parts.append(f"layer_{p}")
            else:
                norm_parts.append(p)

        # All but the last part form the directory; last part is the filename stem
        save_dir = out.joinpath(*norm_parts[:-1])
        save_dir.mkdir(parents=True, exist_ok=True)

        stem = norm_parts[-1]   # "weight" or "bias"
        ext  = ".npy" if save_format == "npy" else ".npz"
        path = save_dir / f"{stem}{ext}"

        if save_format == "npy":
            np.save(path, arr)
        else:
            np.savez_compressed(path, data=arr)

        manifest["files"].append({
            "name":  key,
            "path":  str(path.relative_to(out)),
            "shape": list(arr.shape),
            "ndim":  arr.ndim,
            "dtype": str(arr.dtype)
        })

    # ---- Save embedding matrices separately (optional grouping) ----
    # Comment out this block if a separate embeddings folder is not needed.
    emb_dir = out / "embeddings"
    emb_dir.mkdir(exist_ok=True)
    for subkey, param in model.embeddings.state_dict().items():
        if param.ndim == 2 or (include_bias and param.ndim == 1):
            arr = param.detach().cpu().to(
                dtype=torch.float32 if dtype == "float32" else torch.float64
            ).numpy()
            fname = (
                subkey.replace(".", "_")
                + ("_bias" if subkey.endswith("bias") else "")
                + (".npy" if save_format == "npy" else ".npz")
            )
            path = emb_dir / fname
            if save_format == "npy":
                np.save(path, arr)
            else:
                np.savez_compressed(path, data=arr)

            manifest["files"].append({
                "name":  f"embeddings.{subkey}",
                "path":  str(path.relative_to(out)),
                "shape": list(arr.shape),
                "ndim":  arr.ndim,
                "dtype": str(arr.dtype)
            })

    # ---- Write manifest ----
    with open(out / "manifest.json", "w") as f:
        json.dump(manifest, f, indent=2)

    print(f"Saved to: {out.resolve()}")
    print(f"Total tensors saved: {len(manifest['files'])}")


if __name__ == "__main__":
    extract_matrices(
        model_name="bert-base-uncased",
        out_dir="bert_weights",
        include_bias=False,
        only_linear_like=True,
        dtype="float32",
        save_format="npy",
    )


