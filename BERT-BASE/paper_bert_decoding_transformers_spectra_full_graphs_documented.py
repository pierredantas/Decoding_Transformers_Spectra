    # Diagonal reference: perfect calibration (empirical rate = nominal α)
    ax.plot(alphas, alphas, color="black", linestyle="--", linewidth=0.8)

    ax.set_title(shape_name, pad=2)
    ax.set_ylim(0, 0.2)
    ax.set_xticks(alphas)
